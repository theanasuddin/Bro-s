    private void txtInputField1CaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtInputField1CaretUpdate
        // TODO add your handling code here:
        if (txtInputField1.getText().length() != 0) {
            txtInputField1Disabled.setVisible(false);

            if (isLong(txtInputField1.getText().trim().replaceAll(" +", "")) || isDouble(txtInputField1.getText().trim().replaceAll(" +", ""))) {
                try {
                    if (Long.parseLong(txtInputField1.getText().trim().replaceAll(" +", "")) >= 0) {
                        individualHomeRentField = Double.parseDouble(txtInputField1.getText().trim().replaceAll(" +", ""));
                        lblNextEnabled1 = true;
                    }
                    if (Long.parseLong(txtInputField1.getText().trim().replaceAll(" +", "")) < 0) {
                        lblNext.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/theanasuddin/bros/images/next_button.png")));
                        lblNext.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                        lblNextEnabled = false;
                        lblNextEnabled1 = false;

                    }

                } catch (Exception e) {
                    if (Double.parseDouble(txtInputField1.getText().trim().replaceAll(" +", "")) >= 0) {
                        individualHomeRentField = Double.parseDouble(txtInputField1.getText().trim().replaceAll(" +", ""));
                        lblNextEnabled1 = true;
                    }
                    if (Double.parseDouble(txtInputField1.getText().trim().replaceAll(" +", "")) < 0) {
                        lblNext.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/theanasuddin/bros/images/next_button.png")));
                        lblNext.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                        lblNextEnabled = false;
                        lblNextEnabled1 = false;

                    }
                }

            } else {
                lblNext.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/theanasuddin/bros/images/next_button.png")));
                lblNext.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                lblNextEnabled = false;
                lblNextEnabled1 = false;

            }

        } else {
            txtInputField1Disabled.setVisible(true);
        }

        if (txtInputField1.getText().length() == 0 || txtInputField2.getText().length() == 0 || txtInputField3.getText().length() == 0 || txtInputField4.getText().length() == 0) {
            lblNext.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/theanasuddin/bros/images/next_button.png")));
            lblNext.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
            lblNextEnabled = false;
            lblNextEnabled1 = false;
        }
        if (lblNextEnabled1 && lblNextEnabled2 && lblNextEnabled3 && lblNextEnabled4) {
            lblNext.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/theanasuddin/bros/images/next_button_activated.png")));
            lblNext.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
            lblNextEnabled = true;
        }

    }//GEN-LAST:event_txtInputField1CaretUpdate

