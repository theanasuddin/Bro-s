    private void lblNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblNextMouseClicked
        // TODO add your handling code here:
        if (lblNextEnabled) {
            if (j <= GetBrosNumber.numberOfMembers) {

                if (j % 100 >= 11 && j % 100 <= 20) {
                    ordinal = j + "th";
                } else if (j % 10 == 1) {
                    ordinal = j + "st";
                } else if (j % 10 == 2) {
                    ordinal = j + "nd";
                } else if (j % 10 == 3) {
                    ordinal = j + "rd";
                } else {
                    ordinal = j + "th";
                }
                GetBrosDetails restBros = new GetBrosDetails(ordinal, j);
                restBros.setVisible(true);
                GetBrosDetails.membersName.add(GetBrosDetails.membersNameField);
                GetBrosDetails.individualHomeRent.add(GetBrosDetails.individualHomeRentField);
                GetBrosDetails.individualBazaar.add(GetBrosDetails.individualBazaarField);
                GetBrosDetails.individualMeal.add(GetBrosDetails.individualMealField);
                lblWelcome.setText("Continue..");
                lblNextEnabled = false;
                this.dispose();
            } else {
                GetBrosDetails.membersName.add(GetBrosDetails.membersNameField);
                GetBrosDetails.individualHomeRent.add(GetBrosDetails.individualHomeRentField);
                GetBrosDetails.individualBazaar.add(GetBrosDetails.individualBazaarField);
                GetBrosDetails.individualMeal.add(GetBrosDetails.individualMealField);
                this.dispose();
                new GetOtherBills().setVisible(true);
            }
        }
    }//GEN-LAST:event_lblNextMouseClicked

