    private void txtInputField1CaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtInputField1CaretUpdate
        // TODO add your handling code here:
        if (txtInputField1.getText().length() != 0) {
            txtInputField1Disabled.setVisible(false);
            if (txtInputField2OK) {
                try {
                    if (Integer.parseInt(txtInputField1.getText().trim().replaceAll(" +", "")) > 0) {
                        lblGo.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/theanasuddin/bros/images/go_button_activated.png")));
                        lblGo.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
                        lblGoEnabled = true; 
                        numberOfMembersField = Integer.parseInt(txtInputField1.getText().trim().replaceAll(" +", ""));
                        txtInputField1OK = true;

                    } else if (Integer.parseInt(txtInputField1.getText().trim().replaceAll(" +", "")) <= 0) {
                        lblGo.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/theanasuddin/bros/images/go_button.png")));
                        lblGo.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                        lblGoEnabled = false;
                        txtInputField1OK = false;

                    }
                } catch (Exception e) {

                }
            }
        } else {
            txtInputField1Disabled.setVisible(true);
        }

        if (txtInputField1.getText().length() == 0) {
            lblGo.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/theanasuddin/bros/images/go_button.png")));
            lblGo.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
            lblGoEnabled = false;
            txtInputField1OK = false;
        }
    }//GEN-LAST:event_txtInputField1CaretUpdate

    private void lblGoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblGoMouseClicked
        // TODO add your handling code here:
        if (lblGoEnabled) {
            if (txtInputField2.getText().trim().equals(name)) {
                this.dispose();
                numberOfMembers = numberOfMembersField;
                if (i <= numberOfMembers) {

                    if (i % 100 >= 11 && i % 100 <= 20) {
                        ordinal = i + "th";
                    } else if (i % 10 == 1) {
                        ordinal = i + "st";
                    } else if (i % 10 == 2) {
                        ordinal = i + "nd";
                    } else if (i % 10 == 3) {
                        ordinal = i + "rd";
                    } else {
                        ordinal = i + "th";
                    }

                    GetBrosDetails firstBro = new GetBrosDetails(ordinal, i);
                    firstBro.setVisible(true);
                }

            } else if(txtInputField2.getText().trim().replaceAll(" +", "").length() != 0) {
                this.dispose();
                name = txtInputField2.getText().trim().replaceAll(" +", " ");
                name = name.substring(0, 1).toUpperCase() + name.substring(1).toLowerCase();
                numberOfMembers = numberOfMembersField;
                if (i <= numberOfMembers) {

                    if (i % 100 >= 11 && i % 100 <= 20) {
                        ordinal = i + "th";
                    } else if (i % 10 == 1) {
                        ordinal = i + "st";
                    } else if (i % 10 == 2) {
                        ordinal = i + "nd";
                    } else if (i % 10 == 3) {
                        ordinal = i + "rd";
                    } else {
                        ordinal = i + "th";
                    }
                    GetBrosDetails firstBro = new GetBrosDetails(ordinal, i);
                    firstBro.setVisible(true);
                }

            }
        }
    }//GEN-LAST:event_lblGoMouseClicked

    private void txtInputField2CaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtInputField2CaretUpdate
        // TODO add your handling code here:
        if (txtInputField2.getText().length() != 0) {
            txtInputField2Disabled.setVisible(false);
            if (txtInputField1OK) {
                try {
                    if (validateString(txtInputField2.getText().trim().replaceAll(" +", "")) == true) {
                        lblGo.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/theanasuddin/bros/images/go_button_activated.png")));
                        lblGo.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
                        lblGoEnabled = true;
                        txtInputField2OK = true;
                        name = txtInputField2.getText().trim().replaceAll(" +", " ");

                    } else if (validateString(txtInputField2.getText().trim().replaceAll(" +", "")) == false) {
                        lblGo.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/theanasuddin/bros/images/go_button.png")));
                        lblGo.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                        lblGoEnabled = false;
                        txtInputField2OK = false;

                    }
                } catch (Exception e) {

                }
            }
        } else {
            txtInputField2Disabled.setVisible(true);
        }

        if (txtInputField2.getText().trim().length() == 0 && txtInputField1OK) {
            lblGo.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/theanasuddin/bros/images/go_button_activated.png")));
            lblGo.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
            lblGoEnabled = true;
        }
    }//GEN-LAST:event_txtInputField2CaretUpdate

