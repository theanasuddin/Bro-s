    private void lblNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblNextMouseClicked
        // TODO add your handling code here:
        if (lblNextEnabled) {
            this.dispose();
//            constructPnlUtilityBill();

            pnlInsideCarousel object = new pnlInsideCarousel();
            object.lblItemOne.setText("Electricity");
            object.lblItemTwo.setText("Internet");
            object.lblItemThree.setText("Gas");
            object.lblItemFour.setText("Water");
            object.lblItemFive.setText("Maid");
            object.lblItemOneValue.setText(String.format("%.2f", electricityBill));
            object.lblItemTwoValue.setText(String.format("%.2f", internetBill));
            object.lblItemThreeValue.setText(String.format("%.2f", gasBill));
            object.lblItemFourValue.setText(String.format("%.2f", waterBill));
            object.lblItemFiveValue.setText(String.format("%.2f", housemaidBill));
            pnlInsideCarousel objectTwo = new pnlInsideCarousel();
            objectTwo.lblItemOne.setText("Stairs");
            objectTwo.lblItemTwo.setText("Welfare");
            objectTwo.lblItemOneValue.setText(String.format("%.2f", stairCleaningBill));
            objectTwo.lblItemTwoValue.setText(String.format("%.2f", welfareAssociationBill));

            DisplayOutput output = new DisplayOutput();
            output.pnlUtilityBillContainer.add(output.lblUtilityBillLeft, object);
            output.pnlUtilityBillContainer.add(output.lblUtilityBillRight, objectTwo);
            totalUtilityBill = getTotalUtilityBill();
            totalHomeRent = getTotalHomeRent();
            individualUtilityBill = getIndividualUtilityBill();
            totalMeal = getTotalMeal();
            totalBazaar = getTotalBazaar();
            output.lblMealValue.setText(String.format("%.2f", totalMeal));
            output.lblBazaarValue.setText(String.format("%.2f", totalBazaar));
            output.lblMealRateValue.setText(String.format("%.2f", totalBazaar / totalMeal));
            output.lblHighestMealValue.setText(getHighestMealHolder());
            output.lblTotalUtilityBillValue.setText(String.format("%.2f", totalUtilityBill));
            output.lblTotalHomeRentValue.setText(String.format("%.2f", totalHomeRent));
            output.lblIndividualUtilityBillValue.setText(String.format("%.2f", individualUtilityBill));
            //pnlInsideCarousel for Home Rent
            int carouselAmountForHomeRent = (int) Math.ceil((double) GetBrosNumber.numberOfMembersField / (double) 5);

            pnlInsideCarouselDynamic[] pnlInsideCarouselDynamicObject = new pnlInsideCarouselDynamic[carouselAmountForHomeRent];
            for (int i = 0; i < carouselAmountForHomeRent; i++) {
                pnlInsideCarouselDynamicObject[i] = new pnlInsideCarouselDynamic();
                for (int j = 0; j < 5; j++) {
                    pnlInsideCarouselDynamicObject[i].lblItem[j].setText(GetBrosDetails.membersName.get(arrayListCounter));
                    pnlInsideCarouselDynamicObject[i].lblItemValue[j].setText(String.format("%.2f", GetBrosDetails.individualHomeRent.get(arrayListCounter)));
                    arrayListCounter++;
                    if (arrayListCounter == GetBrosDetails.membersName.size()) {
                        break;
                    }

                }

            }
            output.pnlHomeRentContainer.add(output.lblHomeRentLeft, pnlInsideCarouselDynamicObject[0]);

            if (carouselAmountForHomeRent == 2) {
                output.pnlHomeRentContainer.add(output.lblHomeRentRight, pnlInsideCarouselDynamicObject[1]);
            }
            //individual utility bill
            pnlInsideCarouselDynamic[] pnlInsideCarouselDynamicObjectIUB = new pnlInsideCarouselDynamic[carouselAmountForHomeRent];
            arrayListCounter = 0;
            for (int i = 0; i < carouselAmountForHomeRent; i++) {
                pnlInsideCarouselDynamicObjectIUB[i] = new pnlInsideCarouselDynamic();
                for (int j = 0; j < 5; j++) {
                    pnlInsideCarouselDynamicObjectIUB[i].lblItem[j].setText(GetBrosDetails.membersName.get(arrayListCounter));
                    pnlInsideCarouselDynamicObjectIUB[i].lblItemValue[j].setText(String.format("%.2f", individualUtilityBill));
                    arrayListCounter++;
                    if (arrayListCounter == GetBrosDetails.membersName.size()) {
                        break;
                    }

                }

            }
            output.pnlIndividualUtilityBillContainer.add(output.lblIndividualUtilityBillLeft, pnlInsideCarouselDynamicObjectIUB[0]);

            if (carouselAmountForHomeRent == 2) {
                output.pnlIndividualUtilityBillContainer.add(output.lblIndividualUtilityBillRight, pnlInsideCarouselDynamicObjectIUB[1]);
            }
            //individual home rent plus utility bill

            pnlInsideCarouselDynamic[] pnlInsideCarouselDynamicObjectIHRUB = new pnlInsideCarouselDynamic[carouselAmountForHomeRent];
            arrayListCounter = 0;
            for (int i = 0; i < carouselAmountForHomeRent; i++) {
                pnlInsideCarouselDynamicObjectIHRUB[i] = new pnlInsideCarouselDynamic();
                for (int j = 0; j < 5; j++) {
                    pnlInsideCarouselDynamicObjectIHRUB[i].lblItem[j].setText(GetBrosDetails.membersName.get(arrayListCounter));
                    pnlInsideCarouselDynamicObjectIHRUB[i].lblItemValue[j].setText(String.format("%.2f", individualUtilityBill + GetBrosDetails.individualHomeRent.get(arrayListCounter)));
                    arrayListCounter++;
                    if (arrayListCounter == GetBrosDetails.membersName.size()) {
                        break;
                    }

                }

            }
            output.pnlIndividualHomeRentUtilityBillContainer.add(output.lblIndividualHomeRentUtilityBillLeft, pnlInsideCarouselDynamicObjectIHRUB[0]);

            if (carouselAmountForHomeRent == 2) {
                output.pnlIndividualHomeRentUtilityBillContainer.add(output.lblIndividualHomeRentUtilityBillRight, pnlInsideCarouselDynamicObjectIHRUB[1]);
            }
            //individual meal expanses
            pnlInsideCarouselDynamicIndividual[] pnlInsideCarouselDynamicIndividualObject = new pnlInsideCarouselDynamicIndividual[GetBrosNumber.numberOfMembersField];
            arrayListCounter = 0;
            for (int i = 0; i < GetBrosNumber.numberOfMembersField; i++) {
                pnlInsideCarouselDynamicIndividualObject[i] = new pnlInsideCarouselDynamicIndividual();
                pnlInsideCarouselDynamicIndividualObject[i].lblName.setText(GetBrosDetails.membersName.get(i));
                pnlInsideCarouselDynamicIndividualObject[i].lblItemTwoValue.setText(String.format("%.2f", GetBrosDetails.individualBazaar.get(i)));
                pnlInsideCarouselDynamicIndividualObject[i].lblItemThreeValue.setText(String.format("%.2f", GetBrosDetails.individualMeal.get(i)));
                pnlInsideCarouselDynamicIndividualObject[i].lblItemFourValue.setText(String.format("%.2f", GetBrosDetails.individualMeal.get(i) * (totalBazaar / totalMeal)));
                pnlInsideCarouselDynamicIndividualObject[i].lblItemFiveValue.setText(String.format("%.2f", Math.abs(GetBrosDetails.individualBazaar.get(i) - (GetBrosDetails.individualMeal.get(i) * (totalBazaar / totalMeal)))));
                
                if((GetBrosDetails.individualBazaar.get(i) - (GetBrosDetails.individualMeal.get(i) * (totalBazaar / totalMeal))) <= 0){
                    pnlInsideCarouselDynamicIndividualObject[i].lblItemFive.setText("Debt");
                }
                else if((GetBrosDetails.individualBazaar.get(i) - (GetBrosDetails.individualMeal.get(i) * (totalBazaar / totalMeal))) > 0){
                    pnlInsideCarouselDynamicIndividualObject[i].lblItemFive.setText("Credit");
                }

            }
            output.pnlIndividualMealExpansesContainer.add(output.lblIndividualMealExpansesLeft, pnlInsideCarouselDynamicIndividualObject[0]);
            if(GetBrosNumber.numberOfMembersField == 2){
                output.pnlIndividualMealExpansesContainer.add(output.lblIndividualMealExpansesRight, pnlInsideCarouselDynamicIndividualObject[1]);
            }

            output.setVisible(true);
        }
    }//GEN-LAST:event_lblNextMouseClicked

