    public double getTotalUtilityBill() {
        double totalUtilityBill;
        totalUtilityBill = gasBill + waterBill + stairCleaningBill + welfareAssociationBill + housemaidBill + internetBill + electricityBill;
        return totalUtilityBill;
    }

    public double getIndividualUtilityBill() {
        double individualUtilityBill;
        individualUtilityBill = totalUtilityBill / GetBrosNumber.numberOfMembersField;
        return individualUtilityBill;
    }

    public double getTotalHomeRent() {
        double totalHomeRent = 0;
        for (double individualHomeRent : GetBrosDetails.individualHomeRent) {
            totalHomeRent = totalHomeRent + individualHomeRent;
        }
        return totalHomeRent;
    }

    public double getTotalMeal() {
        double totalMeal = 0;
        for (double individualMeal : GetBrosDetails.individualMeal) {
            totalMeal = totalMeal + individualMeal;
        }
        return totalMeal;
    }

    public double getTotalBazaar() {
        double totalBazaar = 0;
        for (double individualBazaar : GetBrosDetails.individualBazaar) {
            totalBazaar = totalBazaar + individualBazaar;
        }
        return totalBazaar;
    }

    public String getHighestMealHolder() {
        return GetBrosDetails.membersName.get(GetBrosDetails.individualMeal.indexOf(Collections.max(GetBrosDetails.individualMeal)));
    }

